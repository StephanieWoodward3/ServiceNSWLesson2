let students = ["Dave","Christine","Haley"]; //lists the elements in the array
console.log(students);
console.log(students[1]); //calls out the value in position 2 of the array because arrays start at 0
students[1] = "Chris"; //changes the name of position 2 in the array
console.log(students);


students.push("Daniel"); //adds a new student at the end of the array
console.log(students);

students.pop(); //removes a student from the end of the array
console.log(students.pop()); //will tell u what value its removing
console.log(students);